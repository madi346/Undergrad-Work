    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // declare variables
        // dataType variableName = initialValue;
        int value1 = 0; // initializing a good practice
        int value2 = 0;
        int result = 0;
        
        // input - get 2 values from the text boxes
        // we need to convert a string to integer value
        value1 = Integer.parseInt( txtValue1.getText() );
        value2 = Integer.parseInt( txtValue2.getText() );
        
        // process - multiply the values
        result = value1 * value2;
        
        // output - display the result
        // note we have to convert an integer back to a string value
        lblResult.setText( Integer.toString(result) );
    }//GEN-LAST:event_btnCalculateActionPerformed

    
    
    
